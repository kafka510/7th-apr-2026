"""
Timeseries Writer Utility

Writes loss calculation results to timeseries_data table (TimescaleDB).
"""
from typing import Optional, Dict, Any, List
import logging
from datetime import datetime
import uuid
import io
import csv
from django.db import connection, transaction
from django.utils import timezone

from .metric_mapping_service import MetricMappingService

logger = logging.getLogger(__name__)


STAGING_TABLE = "staging_timeseries"
CREATE_STAGING_TEMP_TABLE_SQL = f"""
CREATE TEMP TABLE IF NOT EXISTS {STAGING_TABLE} (
    id uuid NOT NULL,
    device_id text NOT NULL,
    ts timestamptz NOT NULL,
    oem_metric text NOT NULL,
    metric text NOT NULL,
    value text NOT NULL
);
"""


class TimeseriesWriter:
    """
    Utility class for writing calculation results to timeseries_data table.
    
    Uses metric mappings from device_mapping to determine correct metric names.
    """
    
    def __init__(self):
        self.metric_mapper = MetricMappingService()
        self.mappings = self.metric_mapper.get_metric_mappings()

    def _resolve_oem_metric(self, device_type: str, metric_name: str) -> str:
        """
        Resolve oem_metric for a standardized metric name.

        Note: For weather writes (e.g. transposed `gii`) we intentionally skip the mapping
        lookup to avoid noisy logs when weather mappings are not configured.
        """
        dt = (device_type or "").lower()
        # For weather and inverter-level writes we intentionally skip metric mapping
        # to avoid noisy logs when mappings are not configured. Use metric_name as-is.
        if dt in {"weather", "inverter"}:
            return metric_name
        mapping = self.metric_mapper.get_metric_for_device_type(
            dt or "string", metric_name, self.mappings
        )
        return mapping.get("oem_tag", metric_name) if mapping else metric_name
    
    def write_metric(
        self,
        device_id: str,
        metric_name: str,
        value: float,
        timestamp: Optional[datetime] = None,
        device_type: Optional[str] = None,
        oem_metric: Optional[str] = None,
        units: Optional[str] = None
    ) -> bool:
        """
        Write a single metric value to timeseries_data.
        
        Args:
            device_id: Device ID (e.g., 'IJ_SUB_02.PV.P1-1-L02.JB_02_string_1')
            metric_name: Standardized metric name (e.g., 'string_expected_power')
            value: Numeric value to store
            timestamp: Timestamp (defaults to now)
            device_type: Device type for metric mapping lookup (auto-detected if None)
            oem_metric: OEM metric name (auto-looked up if None)
            units: Units for the metric (optional, for logging)
            
        Returns:
            True if successful, False otherwise
        """
        try:
            # Auto-detect device_type from device_id if not provided
            if device_type is None:
                device_type = self._detect_device_type(device_id)
            
            # Get OEM metric from mapping if not provided
            if oem_metric is None:
                mapping = self.metric_mapper.get_metric_for_device_type(
                    device_type, metric_name, self.mappings
                )
                if mapping:
                    oem_metric = mapping.get('oem_tag', metric_name)
                else:
                    # Fallback: use metric_name as oem_metric
                    oem_metric = metric_name
                    logger.warning(
                        f"No mapping found for {device_type}/{metric_name}, "
                        f"using '{metric_name}' as oem_metric"
                    )
            
            # Use current time if timestamp not provided
            if timestamp is None:
                timestamp = timezone.now()
            
            # Convert value to string (as required by timeseries_data.value field)
            value_str = str(value)
            
            # Insert into timeseries_data
            # Note: id is auto-generated by database, so we don't specify it
            # ON CONFLICT will overwrite existing records for the same device_id, ts, and metric
            with connection.cursor() as cursor:
                try:
                    # Try with ON CONFLICT (requires unique constraint on device_id, ts, metric)
                    cursor.execute("""
                        INSERT INTO timeseries_data (device_id, ts, oem_metric, metric, value)
                        VALUES (%s, %s, %s, %s, %s)
                        ON CONFLICT (device_id, ts, metric) 
                        DO UPDATE SET 
                            value = EXCLUDED.value,
                            oem_metric = EXCLUDED.oem_metric
                    """, [
                        device_id,
                        timestamp,
                        oem_metric,
                        metric_name,
                        value_str
                    ])
                except Exception as conflict_error:
                    # Fallback: Delete existing record first, then insert
                    # This works even without a unique constraint
                    if 'constraint' in str(conflict_error).lower() or 'conflict' in str(conflict_error).lower():
                        logger.debug(
                            f"Unique constraint not found, using DELETE+INSERT fallback for {device_id}/{metric_name} at {timestamp}"
                        )
                        cursor.execute("""
                            DELETE FROM timeseries_data
                            WHERE device_id = %s AND ts = %s AND metric = %s
                        """, [device_id, timestamp, metric_name])
                        
                        cursor.execute("""
                            INSERT INTO timeseries_data (device_id, ts, oem_metric, metric, value)
                            VALUES (%s, %s, %s, %s, %s)
                        """, [
                            device_id,
                            timestamp,
                            oem_metric,
                            metric_name,
                            value_str
                        ])
                    else:
                        raise
            
            return True
            
        except Exception as e:
            logger.error(
                f"Error writing metric {metric_name} for {device_id}: {e}",
                exc_info=True
            )
            return False
    
    def write_batch(
        self,
        device_id: str,
        metrics: Dict[str, float],
        timestamp: Optional[datetime] = None,
        device_type: Optional[str] = None
    ) -> Dict[str, bool]:
        """
        Write multiple metrics for a device in a single transaction.
        
        Args:
            device_id: Device ID
            metrics: Dictionary of {metric_name: value}
            timestamp: Timestamp (defaults to now)
            device_type: Device type for metric mapping lookup
            
        Returns:
            Dictionary of {metric_name: success_status}
        """
        results = {}
        
        try:
            # Auto-detect device_type if not provided
            if device_type is None:
                device_type = self._detect_device_type(device_id)
            
            # Use current time if timestamp not provided
            if timestamp is None:
                timestamp = timezone.now()
            
            # Prepare batch insert
            insert_values = []
            for metric_name, value in metrics.items():
                # Get OEM metric from mapping
                mapping = self.metric_mapper.get_metric_for_device_type(
                    device_type, metric_name, self.mappings
                )
                oem_metric = mapping.get('oem_tag', metric_name) if mapping else metric_name
                
                insert_values.append((
                    device_id,
                    timestamp,
                    oem_metric,
                    metric_name,
                    str(value)
                ))
            
            # Batch insert with overwrite support
            # Note: id is auto-generated by database, so we don't specify it
            # ON CONFLICT will overwrite existing records for the same device_id, ts, and metric
            # This ensures rerunning calculations will update existing values instead of creating duplicates
            with connection.cursor() as cursor:
                try:
                    # Try with ON CONFLICT (requires unique constraint on device_id, ts, metric)
                    # This will UPDATE existing records if they exist, or INSERT if they don't
                    cursor.executemany("""
                        INSERT INTO timeseries_data (device_id, ts, oem_metric, metric, value)
                        VALUES (%s, %s, %s, %s, %s)
                        ON CONFLICT (device_id, ts, metric) 
                        DO UPDATE SET 
                            value = EXCLUDED.value,
                            oem_metric = EXCLUDED.oem_metric
                    """, insert_values)
                except Exception as conflict_error:
                    # Fallback: Delete existing records first, then insert
                    # This works even without a unique constraint
                    if 'constraint' in str(conflict_error).lower() or 'conflict' in str(conflict_error).lower():
                        logger.debug(
                            f"Unique constraint not found, using DELETE+INSERT fallback for batch write"
                        )
                        # Fallback: Delete existing records first, then insert
                        # This ensures rerunning calculations will overwrite existing values
                        # Delete all existing records for this device/timestamp/metric combination
                        for device_id_val, ts_val, oem_metric_val, metric_val, value_val in insert_values:
                            cursor.execute("""
                                DELETE FROM timeseries_data
                                WHERE device_id = %s AND ts = %s AND metric = %s
                            """, [device_id_val, ts_val, metric_val])
                        
                        # Now insert all new values (this overwrites any existing data)
                        cursor.executemany("""
                            INSERT INTO timeseries_data (device_id, ts, oem_metric, metric, value)
                            VALUES (%s, %s, %s, %s, %s)
                        """, insert_values)
                    else:
                        raise
            
            # Mark all as successful
            results = {metric: True for metric in metrics.keys()}
            
        except Exception as e:
            logger.error(
                f"Error writing batch metrics for {device_id}: {e}",
                exc_info=True
            )
            # Mark all as failed
            results = {metric: False for metric in metrics.keys()}
        
        return results
    
    def write_loss_calculation(
        self,
        device_id: str,
        expected_power: float,
        actual_power: Optional[float] = None,
        timestamp: Optional[datetime] = None,
        device_type: Optional[str] = None,
        additional_metrics: Optional[Dict[str, float]] = None
    ) -> bool:
        """
        Write a complete loss calculation result (expected + actual + loss).
        
        Args:
            device_id: Device ID
            expected_power: Expected power (W)
            actual_power: Actual power (W), optional
            timestamp: Timestamp (defaults to now)
            device_type: Device type ('string', 'jb', 'inverter')
            additional_metrics: Optional dict of additional metrics to write
            
        Returns:
            True if successful, False otherwise
        """
        try:
            # Auto-detect device_type if not provided
            if device_type is None:
                device_type = self._detect_device_type(device_id)
            
            # Build metrics dictionary
            metrics = {
                f'{device_type}_expected_power': expected_power
            }
            
            if actual_power is not None:
                metrics[f'{device_type}_actual_power'] = actual_power
                
                # Calculate loss
                power_loss = expected_power - actual_power
                metrics[f'{device_type}_power_loss'] = power_loss
                
                # Calculate loss percentage
                if expected_power > 0:
                    loss_percentage = (power_loss / expected_power) * 100
                    metrics[f'{device_type}_loss_percentage'] = loss_percentage
            
            # Add additional metrics if provided
            if additional_metrics:
                metrics.update(additional_metrics)
            
            # Write batch
            results = self.write_batch(device_id, metrics, timestamp, device_type)
            
            # Return True if all writes succeeded
            return all(results.values())
            
        except Exception as e:
            logger.error(
                f"Error writing loss calculation for {device_id}: {e}",
                exc_info=True
            )
            return False

    def write_loss_range_with_staging(
        self,
        device_id: str,
        rows: List[tuple],
        start_ts: datetime,
        end_ts: datetime,
        device_type: str = "string",
        delete_existing: bool = True,
    ) -> bool:
        """
        Write a range of loss-calculation metrics for a device using a staging table.

        - rows: list of (timestamp, metrics_dict) where metrics_dict maps metric_name -> value
        - Deletes existing loss metrics for (device_id, [start_ts, end_ts]) and then
          inserts all provided rows in a single transaction.
        - Uses a session-scoped TEMP table (staging_timeseries) per DB connection.
        """
        if not rows:
            return True

        try:
            with connection.cursor() as cursor:
                # Ensure session-scoped temp staging table exists
                cursor.execute(CREATE_STAGING_TEMP_TABLE_SQL)
                # Clear any previous content for this connection/session
                cursor.execute(f"TRUNCATE {STAGING_TABLE}")

                buf = io.StringIO()
                writer = csv.writer(buf)

                metric_names: set[str] = set()
                expected_count = 0

                for ts, metrics in rows:
                    if not metrics:
                        continue
                    for metric_name, value in metrics.items():
                        oem_metric = self._resolve_oem_metric(device_type, metric_name)

                        writer.writerow(
                            [
                                str(uuid.uuid4()),
                                device_id,
                                ts.isoformat(),
                                oem_metric,
                                metric_name,
                                str(value),
                            ]
                        )
                        metric_names.add(metric_name)
                        expected_count += 1

                if expected_count == 0:
                    cursor.execute(f"TRUNCATE {STAGING_TABLE}")
                    return True

                buf.seek(0)
                cursor.copy_expert(
                    f"""
                    COPY {STAGING_TABLE} (id, device_id, ts, oem_metric, metric, value)
                    FROM STDIN WITH (FORMAT csv)
                    """,
                    buf,
                )

                # Validate staging contents
                cursor.execute(f"SELECT COUNT(*) FROM {STAGING_TABLE}")
                (count,) = cursor.fetchone()
                if count != expected_count:
                    logger.error(
                        "write_loss_range_with_staging: staging count %s != expected %s for %s",
                        count,
                        expected_count,
                        device_id,
                    )
                    cursor.execute(f"TRUNCATE {STAGING_TABLE}")
                    return False

                metric_list = list(metric_names)

                # Atomic replace for this device and metric set in the given time window
                with transaction.atomic():
                    if delete_existing:
                        cursor.execute(
                            """
                            DELETE FROM timeseries_data
                            WHERE device_id = %s
                              AND ts >= %s
                              AND ts <= %s
                              AND metric = ANY(%s)
                            """,
                            [device_id, start_ts, end_ts, metric_list],
                        )
                    cursor.execute(
                        f"""
                        INSERT INTO timeseries_data (device_id, ts, oem_metric, metric, value)
                        SELECT device_id, ts, oem_metric, metric, value
                        FROM {STAGING_TABLE}
                        """
                    )

                cursor.execute(f"TRUNCATE {STAGING_TABLE}")

            return True
        except Exception as e:
            logger.error(
                "Error in write_loss_range_with_staging for %s: %s",
                device_id,
                e,
                exc_info=True,
            )
            try:
                with connection.cursor() as cursor:
                    cursor.execute(f"TRUNCATE {STAGING_TABLE}")
            except Exception:
                # Best-effort cleanup; ignore failure
                pass
            return False
    
    def _detect_device_type(self, device_id: str) -> str:
        """
        Auto-detect device type from device_id.
        
        Args:
            device_id: Device ID string
            
        Returns:
            Device type ('string', 'jb', 'inverter', 'weather')
        """
        device_id_lower = device_id.lower()
        
        if 'string' in device_id_lower:
            return 'string'
        elif 'jb' in device_id_lower or 'junction' in device_id_lower:
            return 'jb'
        elif 'inv' in device_id_lower or 'inverter' in device_id_lower:
            return 'inverter'
        elif 'weather' in device_id_lower or 'meteo' in device_id_lower:
            return 'weather'
        else:
            # Default to string if unclear
            logger.warning(f"Could not detect device_type from {device_id}, defaulting to 'string'")
            return 'string'

